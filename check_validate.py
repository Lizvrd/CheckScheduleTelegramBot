import re

def validate_group_format(text: str) -> bool:
    """
    Проверяет, соответствует ли текст шаблону группы "Б12-345-6"
    
    Шаблон: Буква "Б" + 2 цифры + тире + 3 цифры + тире + 1 цифра
    
    Args:
        text (str): Текст для проверки
        
    Returns:
        bool: True если текст соответствует шаблону, False в противном случае
    """
    pattern = r'^[Бб]\d{2}-\d{3}-\d$'
    return bool(re.match(pattern, text))

if __name__ == "__main__":
    # Тестовые примеры
    test_cases = [
        ("Б12-345-6", True),   # Правильный формат
        ("б12-345-6", True),   # Правильный формат (маленькая буква)
        ("Б01-001-1", True),   # Правильный формат с нулями
        ("Б1-345-6", False),  # Неверное количество цифр в первой части
        ("Б123-345-6", False), # Слишком много цифр в первой части
        ("Б12-34-6", False),   # Неверное количество цифр во второй части
        ("Б12-3456-6", False), # Слишком много цифр во второй части
        ("Б12-345-67", False), # Слишком много цифр в третьей части
        ("А12-345-6", False),  # Неправильная первая буква
        ("Б12_345_6", False),  # Неправильные разделители
        ("Б12-345", False),   # Отсутствует третья часть
        ("12-345-6", False),   # Отсутствует буква в начале
        ("Б-345-6", False),    # Отсутствуют цифры в первой части
    ]
    
    print("Тестирование валидатора группы:")
    for test_input, expected in test_cases:
        result = validate_group_format(test_input)
        status = "✅" if result == expected else "❌"
        print(f"{status} '{test_input}' -> {result} (ожидалось: {expected})")